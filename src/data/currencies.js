export const CURRENCIES_FINAL = CURRENCIES;

export function formatMoney(amount, currencyCode) {
  const cur = CURRENCIES_FINAL.find(c => c.code === currencyCode) || { symbol: currencyCode };
  const n = Number(amount) || 0;
  const formatted = n.toLocaleString(undefined, { minimumFractionDigits: 0, maximumFractionDigits: 2 });
  // Symbol-based currencies vs code-based
  const spaceless = ['$', '€', '£', '¥', '₹', '₺', '₽', '₦', '₪', '₩', '₱', '₫', '฿', '₡', '₲', '₭', '₮', '₴', '₸', '₼', '₾', '֏', '؋', '﷼'];
  if (spaceless.includes(cur.symbol)) return `${cur.symbol}${formatted}`;
  return `${cur.symbol} ${formatted}`;
}

export function getCurrency(code) {
  return CURRENCIES_FINAL.find(c => c.code === code) || CURRENCIES_FINAL[0];
}
